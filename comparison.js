console.log(5 == 5); // return true

console.log(5 != 5); // return false
console.log(5 != 6); // return true

// less than or equal
console.log(5 <= 6); // return true
console.log(5 <= 5); // return true
console.log(5 <= 4); // return false

// greater than or equal
console.log(5 >= 6); // return false
console.log(5 >= 5); // return true
console.log(5 >= 4); // return true

